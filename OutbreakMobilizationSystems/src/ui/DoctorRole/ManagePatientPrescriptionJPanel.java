    private void btnAddMedicineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddMedicineActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMenu.getSelectedRow();
        if (selectedRow >= 0)
        {
            FoodItem foodItem = (FoodItem) tblMenu.getValueAt(selectedRow, 1);
            cart.add(foodItem);
            JOptionPane.showMessageDialog(null, "Food Item " + foodItem.getName()+ " added to cart successfully!");
            totalAmount = totalAmount + foodItem.getPrice();
            populateOrder();
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Please select a row!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnAddMedicineActionPerformed

    private void btnDeleteMedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteMedActionPerformed
        // TODO add your handling code here:
        int selectedRow1 = tblOrder.getSelectedRow();
        if (selectedRow1 >= 0)
        {
            FoodItem foodItem = (FoodItem) tblOrder.getValueAt(selectedRow1, 1);
            cart.remove(foodItem);
            JOptionPane.showMessageDialog(null, "Food Item " + foodItem.getName()+ " deleted from cart Successfully!");
            totalAmount = totalAmount - foodItem.getPrice();
            populateOrder();
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Please select a row!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

    }//GEN-LAST:event_btnDeleteMedActionPerformed

